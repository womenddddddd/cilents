package cn.pojo;

public class Employee {
    /**
     * 销售员编号
     */
    private int Employee_id;
    /**
     * 销售员等级
     */
    private int Employee_age;
    /**
     * 销售员姓名
     */
    private String Employee_name;
    /**
     * 销售员电话
     */
    private String Employee_phone;
    /**
     * 销售员密码
     */
    private String Employee_pwd;
    /**
     * 销售员邮箱
     */
    private String Employee_email;
    /**
     * 销售员出生日期
     */
    private String Employee_birthday;
    /**
     * 销售员地址
     */
    private String Employee_address;
    /**
     * 销售员地址
     */
    private int Employee_Level;

    public int getEmployee_id() {
        return Employee_id;
    }

    public void setEmployee_id(int employee_id) {
        Employee_id = employee_id;
    }

    public int getEmployee_age() {
        return Employee_age;
    }

    public void setEmployee_age(int employee_age) {
        Employee_age = employee_age;
    }

    public String getEmployee_name() {
        return Employee_name;
    }

    public void setEmployee_name(String employee_name) {
        Employee_name = employee_name;
    }

    public String getEmployee_phone() {
        return Employee_phone;
    }

    public void setEmployee_phone(String employee_phone) {
        Employee_phone = employee_phone;
    }

    public String getEmployee_pwd() {
        return Employee_pwd;
    }

    public void setEmployee_pwd(String employee_pwd) {
        Employee_pwd = employee_pwd;
    }

    public String getEmployee_email() {
        return Employee_email;
    }

    public void setEmployee_email(String employee_email) {
        Employee_email = employee_email;
    }

    public String getEmployee_birthday() {
        return Employee_birthday;
    }

    public void setEmployee_birthday(String employee_birthday) {
        Employee_birthday = employee_birthday;
    }

    public String getEmployee_address() {
        return Employee_address;
    }

    public void setEmployee_address(String employee_address) {
        Employee_address = employee_address;
    }

    public int getEmployee_Level() {
        return Employee_Level;
    }

    public void setEmployee_Level(int employee_Level) {
        Employee_Level = employee_Level;
    }
}
