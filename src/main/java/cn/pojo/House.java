package cn.pojo;

public class House {
    /**
     * 房屋编号
     */
    private int h_id;
    /**
     * 房屋名称
     */
    private int h_name;
    /**
     * 房屋类型
     */
    private int h_type;
    /**
     * 房屋价格
     */
    private int h_money;
    /**
     * 房屋地址
     */
    private int h_address;
    /**
     * 房屋面积
     */
    private int h_area;

    public int getH_id() {
        return h_id;
    }

    public void setH_id(int h_id) {
        this.h_id = h_id;
    }

    public int getH_name() {
        return h_name;
    }

    public void setH_name(int h_name) {
        this.h_name = h_name;
    }

    public int getH_type() {
        return h_type;
    }

    public void setH_type(int h_type) {
        this.h_type = h_type;
    }

    public int getH_money() {
        return h_money;
    }

    public void setH_money(int h_money) {
        this.h_money = h_money;
    }

    public int getH_address() {
        return h_address;
    }

    public void setH_address(int h_address) {
        this.h_address = h_address;
    }

    public int getH_area() {
        return h_area;
    }

    public void setH_area(int h_area) {
        this.h_area = h_area;
    }
}
